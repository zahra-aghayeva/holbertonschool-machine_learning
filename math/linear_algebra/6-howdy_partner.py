#!/usr/bin/env python3
"""Defines a function to concatenate two arrays."""


def cat_arrays(arr1, arr2):
    """Concatenates two arrays of ints/floats into a new list.

    Args:
        arr1 (list): The first list.
        arr2 (list): The second list.

    Returns:
        list: A completely new list containing elements of both arrays.
    """
    return arr1 + arr2
